const buildTranslations = () => {
  const result = {} as Record<Locale, TranslationMap>;
  for (const locale of SUPPORTED_LOCALES) {
    result[locale] = { ...enBase, ...localeOverrides[locale] } as TranslationMap;
  }
  return result;
};

const translations = buildTranslations();

export type TranslationKey = keyof typeof enBase;

let activeLocale: Locale = "en";

export function normalizeLocale(value?: string | null): Locale {
  if (!value) return "en";
  const short = value.toLowerCase().split("-")[0] as Locale;
  return SUPPORTED_LOCALES.includes(short) ? short : "en";
}

function readStoredLocale(): Locale {
  if (typeof window === "undefined") return activeLocale;
  return normalizeLocale(localStorage.getItem(STORAGE_KEY) || navigator.language);
}

export function getCurrentLocale(): Locale {
  if (typeof window !== "undefined") activeLocale = readStoredLocale();
  return activeLocale;
}

export function setCurrentLocaleValue(locale: Locale) {
  activeLocale = locale;
  if (typeof window === "undefined") return;
  localStorage.setItem(STORAGE_KEY, locale);
  document.documentElement.lang = locale;
}

export function translate(key: TranslationKey, params?: Params) {
  const locale = getCurrentLocale();
  const value = translations[locale][key] ?? translations.en[key];
  return typeof value === "function" ? value(params) : value;
}

type I18nContextType = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, params?: Params) => string;
};

const I18nContext = createContext<I18nContextType>({
  locale: "en",
  setLocale: () => {
    throw new Error("setLocale called outside I18nProvider");
  },
  t: translate,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => readStoredLocale());

  useEffect(() => {
    setCurrentLocaleValue(locale);
  }, [locale]);

  const value = useMemo<I18nContextType>(
    () => ({
      locale,
      setLocale: (nextLocale) => {
        setCurrentLocaleValue(nextLocale);
        setLocaleState(nextLocale);
      },
      t: (key, params) => {
        const entry = translations[locale][key] ?? translations.en[key];
        return typeof entry === "function" ? entry(params) : entry;
      },
    }),
    [locale]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}



